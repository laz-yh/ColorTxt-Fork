export function isAiWordcloudStopword(term: string): boolean {
  const t = term.trim();
  if (!t) return true;
  if (t.length === 1 && !/[\u4e00-\u9fff]/.test(t)) return true;
  return AI_WORDCLOUD_STOPWORDS.has(t);
}
